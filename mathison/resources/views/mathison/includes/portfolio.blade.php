                                <p class="item-intro text-muted">Resistência Ohmica e de Isolamento.</p>
                                <img class="img-responsive img-centered" src="img/portfolio/resist-inside.png" alt="">
                                <p>A análise de resistência ôhmica e de isolamento são utilizados para determinar a integridade de enrolamento de motores, circuitos e cabos, transformadores e chaves de distribuição. O método de teste é determinado pelo tipo de equipamento sendo testado e para isso utiliza-se o Megôhmetro. </p>
                                <p>A resistência ôhmica de um determinado enrolamento consistem na resistência dos condutores do início ao fim de cada fase, sendo que esses valores medidos (por fase do enrolamento) servem para comparar com os valores pré-estabelecidos originalmente e de histórico do mesmo.</p>
                                <p>A temperatura influencia diretamente nos resultados, tendo em vista que, a resistência ôhmica varia com a temperatura e assim necessitam ser medidas sempre com uma referência à temperatura na qual estão sendo medidas. Para isso, no caso de motores e transformadores comerciais, pode-se fazer a leitura em uma dada temperatura e depois convertê-la para uma medida padrão aplicando cálculos específicos ou, então, fazer com que a máquina atinja sua temperatura nominal de funcionamento e a partir disso realizar as medidas.</p>
                                <p>Segundo o manual do fabricante Fluke, um dos maiores fornecedores de equipamentos de medição de resistência e de isolação, por exemplo, ao testar cabeamento elétrico ou chave de distribuição (equipamento de baixa capacitância), as correntes de fuga capacitiva dependentes do tempo e de fuga de absorção tornam-se insignificantes e decrescem a zero quase instantaneamente.
                                Um fluxo de corrente de fuga condutiva estável é alcançado quase instantaneamente (um minuto ou menos), fornecendo condições perfeitas para a leitura de ponto/teste de resistência de curta duração.
