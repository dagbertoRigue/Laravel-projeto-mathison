                                <p>O primeiro a apresentar um trabalho sobre as oscilações foi Galileu Galilei, e sua pesquisa baseou-se em um pêndulo simples e a vibração de cordas, representando uma importante fundamentação para a teoria das vibrações. A ideia surgiu ao observar o comportamento de uma lâmpada em uma igreja de Pisa, com seus movimentos pendulares e ao comparar com sua própria pulsação, constatou que o período era independente da amplitude das oscilações.</p>
                                <p>Qualquer movimento que se repita após um intervalo de tempo é denominado vibração ou oscilação. O balançar de um pêndulo e o movimento de uma corda dedilhada são exemplos típicos de vibração (RAO, 2009, p.2).</p>
                                <p>Em 1686, quando Sir Isaac Newton publicou sua maior obra sobre a lei da gravitação universal, apresentou também as três leis do movimento, tendo a sua segunda lei de Newton como a mais importante para o estudo moderno sobre a vibração, sendo utilizados frequentemente nos livros atuais. </p>
                                <p>Mas, foi Brook Taylor, em 1713, que aperfeiçoou o procedimento utilizado por Galileu, introduzindo derivadas parciais nas equações do movimento de frequências independentes igual ao número de partículas de massa, obtendo assim a solução teórica do problema da corda vibratória, o Teorema de Taylor e a frequência natural.</p>
                                <p>O estudo das vibrações se mostra de grande importância devido aos efeitos que podem ser produzidos. Com o intuito de reduzir ao máximo esses efeitos gerados por máquinas rotativas, torna-se imprescindível conhecer o histórico do equipamento, com o intuito de identificar e avaliar seu potencial de falhas, evitando o mau funcionamento e a quebra do mesmo ou de sua estrutura onde se encontra.</p>
