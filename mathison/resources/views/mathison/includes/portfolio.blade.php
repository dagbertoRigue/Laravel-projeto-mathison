                                <img class="img-responsive img-centered" src="img/portfolio/oleo-preview.png" alt="">
                                <p>A análise de óleos lubrificantes usados tornou-se um dos principais itens dos programas de manutenção preditiva de equipamentos industriais, devido ao alto custo dos componentes mecânicos, acrescido do prejuízo decorrente da paralisação de equipamentos por avarias relacionadas ao lubrificante.</p>
                                <p>A lubrificação é a ciência de controlar o atrito, que é a resistência ao movimento e o controle ao desgaste, tendo o lubrificante como a substância principal de controle do atrito, esfriamento das partes mecânicas, proteção contra ferrugem e a corrosão, vedação das partes em movimento e eliminação dos ruídos. </p>
                                <p>O lubrificante apresenta dois modos de falhas principais, sendo a contaminação por partículas de desgaste do equipamento ou por agentes externos e a degradação das propriedades químicas do lubrificante. Sendo assim, a análise do óleo lubrificante é utilizada visando identificar dois objetivos principais: a condição do óleo e a condição do equipamento.
                                Geralmente, nos relatórios da análise do óleo lubrificante constam a viscosidade, o ponto de fulgor, o índice de viscosidade e a acidez.</p>
